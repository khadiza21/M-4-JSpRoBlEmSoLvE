//  print 1 to 10 natural numbers
console.log("1 to 10 Natural Numbers");
for (let i = 1; i <= 10; i++) {
  console.log(i);
}
//  print 1 to 10 odd numbers
console.log("1 to 10 Odd Numbers");
for (let i = 1; i <= 10; i++) {
  if (i % 2 != 0) {
    console.log(i);
  }
}

//  print 1 to 10 even numbers
console.log("1 to 10 Even Numbers");
for (let i = 2; i <= 10; i++) {
  if (i % 2 == 0) {
    console.log(i);
  }
}
